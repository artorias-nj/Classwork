def rail_fence_d(ciphertext, rail):
    ciphertext=ciphertext.lower().replace(" ", "")
    rail=int(rail)
    pattern=[]
    row=0
    direction=1
    for i in range(len(ciphertext)):
        pattern.append(row)
        if row==0:
            direction=1
        elif row==rail-1:
            direction=-1
        row+=direction
    rails=[[] for i in range(rail)]
    index=0
    for r in range(rail):
        count=pattern.count(r)
        rails[r]=list(ciphertext[index:index+count])
        index+=count
    plaintext=""
    rail_index=[0]*rail
    for r in pattern:
        plaintext+=rails[r][rail_index[r]]
        rail_index[r]+=1
    plaintextE.delete("1.0", "end")
    plaintextE.insert("1.0", plaintext)

