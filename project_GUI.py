def row_transposition_d(ciphertext, key):
    ciphertext=ciphertext.lower().replace(" ", "")
    keylist=[]
    for i in key:
        keylist.append(int(i))
    plaintext=""
    keys=[[] for i in range(len(keylist))]
    keys2=[[] for i in range(len(keylist))]
    max_key=max(keylist)
    cols=len(keylist)
    rows=len(ciphertext)//cols
    iteration=0
    for i in range(cols):
        keys2[i]=list(ciphertext[iteration:iteration+rows])
        iteration+=rows
    for i in range(len(keylist)):
        keys[i]=keys2[keylist[i]-1]
    for r in range(rows):
        for c in range(len(keys)):
            plaintext+=keys[c][r]
    plaintextE.delete("1.0", "end")
    plaintextE.insert("1.0", plaintext)

