import random
to_number = {chr(i + 97): i for i in range(26)}
to_letter = {v: k for k, v in to_number.items()}
atbash = {
    'a':'z', 'b':'y', 'c':'x', 'd':'w', 'e':'v', 'f':'u',
    'g':'t', 'h':'s', 'i':'r', 'j':'q', 'k':'p', 'l':'o',
    'm':'n', 'n':'m', 'o':'l', 'p':'k', 'q':'j', 'r':'i',
    's':'h', 't':'g', 'u':'f', 'v':'e', 'w':'d', 'x':'c',
    'y':'b', 'z':'a'
}
caeser_description="A Ceaser cipher encrypts by shifting each letter k amounts around the alphabet. For emaple, in a key of 1 A=B. To decrypt the cipher simply do k amounts of shifts the other way."
affine_description="The Affine Cipher is a substitution cipher wherein each letter in an alphabet is mapped to its numeric equivalent using the formula E(X) = (aX + b) mod 26. X repesents each letter and a, b represnts the cipher key such that a is relatively prime to 26 (gcd(a, 26)=1), and b is the shift value."
vigenère_description="The Vigenère Cipher is a polyalphabetic substitution cipher that uses a keyword or phrase to encrypt the plaintext by shifting the letters to the corresponding letter of the key."
rail_fence_description="The plaintext is written diagonally across a number of 'rails' (horizontal lines), and the ciphertext is formed by reading the rails then taking each one and adding it one by one to the ciphertext."
atbash_description="An ancient hewbrew cypher, and a simple one, the alphabet is mapped backwords so A=Z, B=Y, C=X and so on."
row_transposition_description="The plaintext is written into a grid row by row, and then the ciphertext is generated by reading the columns in a specific order determined by a key."

def caesar_e(plaintext,key):
    plaintext = plaintext.lower().replace(" ", "")
    key=int(key)
    ciphertext=""
    for i in plaintext:
        if i.isalpha():
            x=to_number[i]
            x=(x+key)%26
            x=to_letter[x]
            ciphertext+=x
        else:
            ciphertext+=i
    ciphertextE.delete("1.0", "end")
    ciphertextE.insert("1.0", ciphertext)

def affine_e(plaintext,ab):
    plaintext = plaintext.lower().replace(" ", "")
    a, b=ab.split(",")
    a=int(a)
    b=int(b)
    ciphertext=""
    for i in plaintext:
        if i.isalpha():
            x=to_number[i]
            x=(a*x+b)%26
            x=to_letter[x]
            ciphertext+=x
        else:
            ciphertext+=i
    ciphertextE.delete("1.0", "end")
    ciphertextE.insert("1.0", ciphertext)
    
def vigenère_e(plaintext,keyword):
    plaintext = plaintext.lower().replace(" ", "")
    keyword=keyword.lower().replace(" ", "")
    key=[]
    for i in keyword:
        x=to_number[i]
        key.append(x)
    ciphertext=""
    for i in range(len(plaintext)):
        if plaintext[i].isalpha():
            x = to_number[plaintext[i]]
            x = (x + key[i % len(key)]) % 26
            x = to_letter[x]
            ciphertext += x
        else:
            ciphertext+=plaintext[i]
    ciphertextE.delete("1.0", "end")
    ciphertextE.insert("1.0", ciphertext)

def rail_fence_e(plaintext,rail):
    plaintext=plaintext.lower().replace(" ", "")
    rail=int(rail)
    ciphertext=""
    rails=[[] for i in range(rail)]
    direction=1
    row=0
    for letter in plaintext:
        rails[row].append(letter)
        if row==0:
            direction=1
        elif row==rail-1:
            direction=-1
        row+=direction
    for r in rails:
        for c in r:
            ciphertext+=c
    ciphertextE.delete("1.0", "end")
    ciphertextE.insert("1.0", ciphertext)

