def row_transposition_e(plaintext,key):
    plaintext=plaintext.lower().replace(" ", "")
    keylist=[]
    for i in key:
        keylist.append(int(i))
    ciphertext=""
    keys=[[] for i in range(len(keylist))]
    keys2=[[] for i in range(len(keylist))]
    max_key=max(keylist)
    iteration=0
    for i in plaintext:
        keys[iteration].append(i)
        iteration+=1
        if iteration==max_key:
            iteration=0
    for i in range (max_key):
        while len(keys[i]) < len(keys[0]):
            x=random.randint(0, 25)
            x=to_letter[x]
            keys[i].append(x)
    for i in range(len(keylist)):
        keys2[keylist[i]-1]=keys[i]
    for i in keys2:
        for j in i:
            ciphertext+=j
    ciphertextE.delete("1.0", "end")
    ciphertextE.insert("1.0", ciphertext)
    
def caesar_d(ciphertext, key):
    ciphertext = ciphertext.lower().replace(" ", "")
    plaintext=""
    key=int(key)
    for i in ciphertext:
        if i.isalpha():
            x=to_number[i]
            x=(x-key)%26
            x=to_letter[x]
            plaintext+=x
        else:
                plaintext+=i
    plaintextE.delete("1.0", "end")
    plaintextE.insert("1.0", plaintext)

def mod_inverse(a, m):
    for i in range(m):
        if (a * i) % m == 1:
            return i

def affine_d(ciphertext,ab):
    ciphertext = ciphertext.lower().replace(" ", "")
    a, b=ab.split(",")
    a=int(a)
    b=int(b)
    plaintext=""
    a_inv = mod_inverse(a, 26)
    for i in ciphertext:
        if i.isalpha():
            x=to_number[i]
            x=(a_inv*(x-b))%26
            x=to_letter[x]
            plaintext+=x
        else:
                plaintext+=i
    plaintextE.delete("1.0", "end")
    plaintextE.insert("1.0", plaintext)
    
